class window():
    def __init__(self,root):
        self.root = root
        self.root.title("ANN Visualizer")
        self.root.resizable(width=False, height=False)

        self.nam = StringVar()

        ttk.Entry(self.root,textvariable=self.nam).pack(fill=X)
        self.nam.set('5-4-2')

        frame = ttk.Frame(self.root)
        frame.pack()
        self.colors=COLORS
        self.b1 = ttk.Button(frame,text='RANDOM COLORS',width=100,command=self.randcolors)

        self.b1.pack(side=RIGHT)
        self.b2=ttk.Button(frame,text='ONLY WHITE',width=100,command=self.onlywhite, style="info.TButton")

        self.b2.pack(side=LEFT)
        self.canvas = Canvas(self.root,width=1200,height=720,bg='white')

        self.canvas.pack(side=LEFT)
        self.scrollbar = ttk.Scrollbar(self.root,orient=VERTICAL,command = self.canvas.yview)
        self.scrollbar.pack(side=RIGHT,fill=Y)
        self.canvas.configure(yscrollcommand=self.scrollbar.set)
        self.canvas.bind('<Configure>',self.__resetscroll)
        
        dosframe = ttk.Frame(self.canvas)
        self.canvas.create_window((0,0),window=dosframe,anchor='nw')
        self.root.bind("<Escape>", lambda a: self.__clear())
        self.root.bind("<Return>", lambda a: self.__drawNode())
        self.root.bind("<Control-s>", lambda a: self.__save())

        self.canvas.bind("<MouseWheel>", self.__mousewheel)
        self.canvas.configure(bg='black')

    def onlywhite(self):
        self.colors=['white','snow']
        self.__drawNode()

    def randcolors(self):
        self.colors=COLORS
        self.__drawNode()

    def __resetscroll(self,e):
        self.canvas.configure(scrollregion=self.canvas.bbox('all'))

    def __mousewheel(self, event):
        self.canvas.yview_scroll(-1*(event.delta//120), "units")

    def __clear(self):
        self.canvas.delete('all')

    def __drawNode(self):
        GAP=20
        scalex=100
        scaley=50
        self.__clear()
        l = list(map(int,self.nam.get().split('-')))

        l2 = [[] for _ in range(len(l))]
  
        l3=[abs(i-max([GAP*i for i in l])) for i in [GAP*i for i in l]]
        x=y=5
        # for drawing each layer with respective number of nodes
        for layer,node in enumerate(l):
            for i in range(node):
                rong=random.choice(self.colors)
                # rongs.append(rong)
                self.canvas.create_oval(layer*scalex + x,i*scaley + y+l3[layer],layer*scalex+GAP + x,i*scaley+GAP + y+l3[layer],
                                            fill=rong)
                l2[layer].append([layer*scalex + x,i*scaley + y+l3[layer],rong,layer*scalex+GAP + x,i*scaley+GAP + y+l3[layer]])
            # for drop in range(int(node*0.2)):
            #     l2[layer][random.randint(0,node-1)][2]='black'

        
        # for the connections between the nodes
        for k in range(len(l2)-1):
            for i in l2[k]:
                for j in l2[k+1]:
                    self.canvas.create_line(i[-2],i[-1]-GAP/2,j[0],j[1]+GAP/2,width=1,fill=i[2])

        self.__resetscroll(self)

    def __save(self):
        name=self.nam.get()
        canvasvg.saveall(name+'.svg',self.canvas)
        




